    хорошо. Понятно, что новое место жительства должно отвечать многим критериям и быть компромиссным по отношению к
    человеку и членам семьи. По отзывам, размещенным на сайте ФМА ПМЖ РФ можно составить целостную картину по любому
    региону. А Россия многообразна. Если вы любите жить в краях – «по теплее», то безусловно можно обратить внимание на
    Краснодарский край. Единственное с работой в приморской зоне будет сложновато. В Калининградской области – наоборот,
    климат умереннее, и ближе к западу. Порой в магазин проще съездить в Польшу или Литву (3-4 часа). Москва и Питер
    понравятся людям амбициозным, выдерживающим не шуточный ритм столиц. Разнообразна жизнь и в городах центрального и
    южного федеральных округов. Одним словом, выбирать есть из чего.
</p>
<p>
    Причины (мы о них писали ранее) - затянувшаяся депрессия, одиночество, отсутствие хорошего места работы или учебы, а

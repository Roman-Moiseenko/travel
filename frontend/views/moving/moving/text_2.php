    стоимости проезда и провоза багажа к месту использования отпуска и обратно
    <br>(в ред. Федерального закона от 30.06.2006 N 90-ФЗ)
</p>
<p>
    Лица, работающие в организациях, расположенных в районах Крайнего Севера и приравненных к ним местностях, имеют
    право на оплату один раз в два года за счет средств работодателя стоимости проезда и провоза багажа в пределах
    территории Российской Федерации к месту использования отпуска и обратно. Право на компенсацию указанных расходов
    возникает у работника одновременно с правом на получение ежегодного оплачиваемого отпуска за первый год работы в
    данной организации.
    <br>(часть первая в ред. Федерального закона от 02.04.2014 N 50-ФЗ)
</p>
<p>
    Федеральные государственные органы, государственные внебюджетные фонды Российской Федерации, федеральные

<h3>Как составить план переезда на ПМЖ и не чего не забыть?</h3>
<p>
    Следующий шаг – составляем план переезда:
</p>
<p>
    1. Определяем, когда и что надо сделать, чтобы ничего не забыть.
</p>
<p>
    2. Продаем или сдаем в аренду недвижимость и имущество, рассчитываемся с работой (бизнесом) + готовим документы,
    которое не планируется перевозить: обычно это автомобиль, мебель, предметы интерьера, книги и т.п. Это обычно самая
    длительная процедура — готовиться надо заранее. Кстати автомобиль можно и не продавать, а спокойно на нем переехать
    жить в другой город. Возможно даже придется какое-то время пожить на съемной квартире. Но главное в этой истории
    собрать и сохранить все необходимые документы. С работы необходим все справки и трудовая книжка. Бизнес – дело
    серьезное. Нужно или продавать, или организовать управление (дистанционное). Обязательно собрать все справки с места
    учебы детей. Не забыть про загранпаспорта и вторую половинку (если вы в разводе), так как при переезде, например,
    Калининградскую область, вам наверняка понадобиться согласие супруга на выезд детей за границу и т.п. (оформляется
    нотариально). Подбейте все финансовые документ и активы (включая кредитные карточки и постоянные платежи (например,
    алименты)). Вы должны заранее позаботиться о снятии с регистрационного учёта (в том числе в военкомате для
    военнообязанных) в Вашем родном городе. Постарайтесь также заранее заменить документы, срок действия которых
    подходит к концу (водительское удостоверение, загранпаспорт и т.п.), чтобы не тратить первое время на эти процедуры.
    Посетите сои поликлиники и больницы и заберите амбулаторные карточки. Они вам пригодятся по новому месту
    прикрепления к лечебному учреждению. Если у вас есть несовершеннолетний ребенок, который является собственником
    жилья, с которого вы собрались съезжать, он едет вместе с вами, у вас могут возникнуть затруднения с органами
    попечительства. По закону, новое жилье, в котором будет жить несовершеннолетний, не должно уступать старому, ни по
    площади, ни по характеристикам. Особенно это актуально в том случае, если старое жилье вы собрались продать. Не
    забывайте и о деликатном – о долгах… В этой жизни – все кому-нибудь что-нибудь должны… Рекомендуем все долговые
    обязательства зафиксировать исполнительными листами через суд. Внимательно просмотреть базы данных ГИБДД и Налоговой

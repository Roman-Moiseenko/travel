<h3>Стресс и трудности адаптации</h3>
<p>
    Стресс и страх - это совершенно естественное состояние. Вопросы, неизвестность, а вдруг не получится, а вдруг не
    приживемся, ах, как жалко родную сковородку-табуретку и т.д., и т.п. Но если у вас уже появилась мысль о переезде,
    если вы ее уже передумали вдоль и поперек, побывали на нашем сайте, пообщались с «коллегами» то значит, пришло время
    действовать. Пора расправить крылья, стряхнуть с них пыль и полететь вперед, навстречу к переменам. Иногда, даже
    может и начать жизнь с «чистого листа»?!?!?
</p>
<p>
    Перемены и неизвестность всегда выбивает из колеи (быта и спокойствия), но по прошествии какого-то времени всегда
    оказывается, что они были жизненно необходимы. Поэтому наш совет — не бойтесь что-то менять в своей жизни. Лучше
    что-то сделать, ошибиться и исправить, если не получилось так, как вы задумали, чем сидеть и до конца жизни кусать
    локти о том, что возможность была, а вы ею не воспользовались. Впрочем, это относится не только к переездам в другой
    город….
</p>
<p>
    В то же время голову терять тоже не стоит. Всегда нужно иметь запасной вариант, мало ли что. Поэтому, предварительно
    – проконсультируйтесь с нашими сотрудниками с круглосуточного колл-центра. Например, решили вы с северов или Сибири
    перебраться на юг. В родном городе у вас квартира, не торопитесь ее продавать. Лучше сдайте на год, а сами поезжайте
    на длительную разведку в город мечты. Там так же снимайте квартиру на год (за ту же сумму – чтобы выйти в ноль, или
    по возможности дешевле – лишним не будет) и изучайте город. Можно, при наличии средств купить ликвидную
    однокомнатную квартиру и переехать в нее.
</p>
<p>
    Безусловно встанет вопрос с зарплатой и работой. Здорово, если у вас есть возможность работать удаленно. А если нет?
    Тогда, разумней опять предварительно проконсультироваться с нашими специалистами и заранее подать заявку на
    трудоустройство. Будьте уверены, мы в трудную минуту поможем, хотя конечно, и мы не Боги.
</p>
<p>
    На новом месте постарайтесь больше общаться с местными жителями. Делайте это везде — в общественном транспорте (даже
    если у вас есть своя машина, иногда полезно выйти в люди), в очередях, просто так на улице. Не забывайте и про
    местные форумы. Бывает, что люди, которые долго живут на одном месте, не особо-то и довольны этим местом. То дороги
    им плохие, то погода, то еще что-то. Так что постарайтесь не сильно впитывать чужой негатив, а стройте свое личное
    мнение о городе. Психологически это делать очень легко. Просто надо говорить свою оценку того, то видите… В
    большинстве своем народ откликается на вашу реплику. Есть и сайты знакомств. Можно поискать земляков и т.п.
</p>
<p>
    Ходите больше пешком, обследуйте окраины, гуляйте вечерами. Потратьте время на объезд всех районов и не забудьте про
    въезды/выезды из города, если вы являетесь любителем загородного отдыха. Загляните в бюджетные заведения: на почту,
    в больницу, если нужно — школу и садик. Для мам лучшим способом узнать все «новости» о городе будет колясочная банда
    на детской площадке, расскажут обо всем, проверено на себе.
</p>
<p>
    Идеальный срок для такой разведки — год. Успеете посмотреть все времена года, проверить реакцию организма на смену
    климата, воды и воздуха, понять, горазды ли вы жить теперь без зимы или, наоборот, со снегом по колено в течение
    полугода. Да и вообще, не заест ли вас ностальгия по родным подворьям.
</p>
<p>
    Переезд зачастую является довольно сложным периодом в жизни человека, это выход из зоны комфорта и умиротворенности,
    а по-простому – стресс (реальный). Постарайтесь быть в это время более терпеливыми и не портить отношения с
    близкими, которые переезжают вместе с вами, им ведь тоже не легко. Адаптация закончится, а неприятный осадок
    останется. Учитесь говорить по душам и понимать друг друга. Будьте гибче и проще, не «зацикливайтесь» на мелочах,
    этим вы облегчите себе переезд. Постарайтесь найти в новом городе какие-то особые, свои места, это поможет быстрее
    привыкнуть к нему. Наше агентство ФМА ПМЖ РФ нацелено на помощь в адаптации в первые год переезда гражданам России и
    мигрантам из-за рубежа.
</p>
<p>
    Переезд – это не стихийное бедствие. Переезд – это ваш осознанный выбор, это сознательный выбор перемен, к которому
    нужно быть готовым не только физически (собрать вещи), но и морально. С открытием нашего агентства большинство
    сложных и не простых моментов будут разрешаться легко и непринужденно.
</p>
<h3>Немного о наших близких и детях</h3>
<p>
    Вам действительно необходимо подготовить своих детей к тому, через что вам предстоит пройти всем вместе. Нужно
    объяснить, чего вы ждёте от них и чего ждать на новом месте им. Пытаясь защитить детей от стресса и не рассказывая
    им заранее о сложностях, которые влечёт за собой смена места жительства, вы не помогаете им, а, напротив, рискуете
    подвергнуть гораздо большему стрессу на новом месте.
</p>
<p>
    В зависимости от возраста дети по-разному переживают покидание дома, в котором, возможно, они провели всю свою
    жизнь. Одно дело — переезд в квартиру, находящуюся недалеко от вашего прежнего места жительства, и совсем другое — в
    другой район или даже город. Детей может пугать тот факт, что они больше не увидят друзей, что придётся идти в новую
    школу, где они никого не знают. Чтобы помочь ребёнку попрощаться с друзьями на позитивной волне, устройте прощальную
    вечеринку, на которую он сможет пригласить всех, кого захочет. И тогда дети запомнят прощание как праздник, а не как
    трагедию.
</p>
<p>
    И не уничтожайте памятные вещи, которые связаны со старой квартирой. Вместе, всей семьёй потрудитесь над созданием
    альбома, слайд-шоу или коробки «с воспоминаниями». В процесс разговаривайте с детьми о будущем – фантазируйте, какой
    прекрасной будет жизнь на новом месте. И спросите детей, чего они больше всего ожидают от нового места — возможно,
    их ответы помогут вам легче адаптировать их к новой жизни.
</p>
<p>
    Не отмахивайтесь от того, что говорят вам дети. Слушайте их и старайтесь, где возможно, сделать так, как хотят
    именно они. Так они поймут, что вы понимаете их тревогу и позаботитесь о том, чтобы на новом месте им было ещё
    лучше, чем на старом. Даже если ваш ребёнок ещё совсем кроха, его маленькие проблемы кажутся ему огромными — не
    забывайте об этом. Сообщите ребёнку как можно больше информации о тех переменах, которые предстоят. Планируя переезд
    с ребенком, зарегистрируйтесь на форуме для того, чтобы Ваш ребенок смог найти друзей в новом городе заранее, чтобы
    он разузнал все, что его интересует от сверстников, заодно наладите собственный круг общения. В день переезда
    соберитесь всей семьёй и ещё раз обсудите, как всё будет происходить, чтобы каждый знал, что от него ожидается и как
    следует себя вести. Постарайтесь для ребенка составить отдельный план переезда. Если вы в разводе – обязательно
    согласуйте со второй половинкой порядок общения с родными.
</p>
<h3>Перевозим имущество и вещи. Едем сами</h3>
<p>

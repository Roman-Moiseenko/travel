    использования отпуска и обратно любым видом транспорта (за исключением такси), в том числе личным, стоимость провоза
    багажа весом до 30 килограммов, а также стоимость проезда и провоза багажа к месту использования отпуска работника и
    обратно неработающим членам его семьи (мужу, жене, несовершеннолетним детям, фактически проживающим с работником)

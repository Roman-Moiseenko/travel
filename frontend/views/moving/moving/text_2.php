<h3>О переезде на ПМЖ в Россию</h3>
<p>
    (Материал с сайта Министерства Иностранных Дел РФ)
</p>
<p>
    Основные подходы к решению задач в сфере миграции определены в «Концепции регулирования миграционных процессов в
    Российской Федерации», утвержденной распоряжением Правительства Российской Федерации от 1 марта 2003г. №256-р. В
    настоящее время в соответствии с Планом реализации Концепции разрабатывается проект Федеральной целевой программы,
    предусматривающей содействие в добровольном переселении соотечественников.
</p>
<p>
    С рядом государств СНГ и Балтии заключены и ратифицированы специальные соглашения, дающие переселенцам некоторые
    льготы (Армения, Белоруссия, Азербайджан, Казахстан, Киргизия, Латвия, Таджикистан, Туркмения, Украина). С Эстонией
    и Грузией такие договора заключены, но не ратифицированы. При Посольствах России в Армении, Латвии, Туркмении,
    Киргизии, Таджикистане открыты Представительства миграционной службы России, прорабатывается вопрос о создании таких
    представительств в Азербайджане, Казахстане, Молдавии и Украине.
</p>
<p>
    Российские граждане могут беспрепятственно переехать на ПМЖ в Россию, однако вопросы своего обустройства им придется
    решать самостоятельно. Как правило, переселенцы приобретают жилье в России за счет средств, вырученных от продажи
    своей квартиры в государстве выезда, либо регистрируются у родственников.
</p>
<p>
    Пенсионеры и инвалиды, имеющие российское гражданство, могут обращаться в Министерство здравоохранения и социального
    развития Российской Федерации или непосредственно в органы социальной защиты населения конкретного субъекта РФ с
    просьбой о размещении в стационарном учреждении социального обслуживания. Вместе с тем на предоставление путевок для
    постоянного проживания в домах-интернатах для престарелых имеются очереди, а условия размещения предполагают
    поселение от 3 до 5 человек в комнате. Участники ВОВ могут претендовать на размещение в домах-интернатах для
    ветеранов, где более комфортные условия (1-2 человека в комнате). Законодательство предусматривает предоставление
    мест в домах-интернатах либо в субъекте РФ по месту жительства на момент устройства (на соотечественников,
    проживающих за рубежом, не распространяется), либо по месту рождения. В противном случае путевки могут быть
    предложены в любом регионе, где имеются свободные места.
</p>
<p>
    Если у заявителей нет гражданства России и права на его приобретение в упрощенном порядке, то они могут решать
    вопросы переезда в рамках Федерального закона «О правовом положении иностранных граждан в Российской Федерации» от
    25.07.2002 года № 115-ФЗ. Лица, не имеющие гражданства России, должны легализовать свое жительство в России, вначале
    оформив разрешение на временное проживание, а впоследствии - вид на жительство. Ходатайство о предоставлении
    разрешения на временное проживание заявители могут подать как через консульские учреждения России за рубежом еще до
    выезда, так и в органы внутренних дел по месту предполагаемого проживания в России непосредственно после приезда в
    РФ. Разрешение на временное проживание оформляется на три года, дает право на работу и может неоднократно
    продлеваться. Вид на жительство можно получить при наличии жилья уже через год после нахождения в России по
    разрешению на временное проживание.
</p>
<p>
    Жилье в России имеет право приобрести любой гражданин (в т.ч. иностранный) независимо от страны его постоянного
    проживания.
</p>
<p>
    В целях трудоустройства граждане, переезжающие на постоянное место жительства в Российскую Федерацию, могут
    обращаться в территориальные органы по вопросам занятости населения, которые в соответствии с пунктом 1 статьи 15
    Закона Российской Федерации от 19 апреля 1991 года № 1032-1 «О занятости населения в Российской Федерации»,
    оказывают содействие гражданам в поиске подходящей работы.
</p>
<h3>Общие советы и рекомендации, переезжающим на ПМЖ</h3>
<p>
    Чтобы быть одновременно зарегистрированным в двух населенных пунктах необходимо в одном зарегистрироваться "по месту
    жительства", а в другом "по месту пребывания"
</p>
<p>
    Для регистрации по месту жительства или по месту пребывания Вам надлежит следовать указаниям из Административного
    регламента предоставления Федеральной миграционной службой государственной услуги по регистрационному учету граждан
    Российской Федерации по месту пребывания и по месту жительства в пределах Российской Федерации, утвержденного
    Приказом ФМС России от 11.09.2012 N 288.
</p>
<p>
    В последнее время часто появляются люди, желающими переехать в сельскую местность и обосноваться там на постоянное
    место жительства. В основном это семейные, возраст от 30 до 50 лет. Приведем советы желающим переехать жить в
    деревню.
</p>
<p>
    Не стоит выбирать для жизни разваливающиеся, очень удаленные от районного центра поселения. Важно наличие медпункта,
    магазина, администрации, интернета. Если вы с детьми, то школа и детский сад.
</p>
<p>
    Ни в коем случае не продавайте городскую квартиру, от ее сдачи вы будете иметь пассивный доход, в случае, если не
    приживетесь в селе, есть пути отступления.
</p>
<p>
    Советуем перед тем, как сделать выбор обратиться в местную областную администрацию. Изложите им свое желание о
    переезде в село, существует программа по переселению (в каждом регионе своя). Воспользовавшись поддержкой, вы
    обустроите свой деревенский быт, решите финансовую проблему переезда. Набьете меньше шишек, а значит жизнь будет
    счастливее и радостней. Для этого мы и едем в деревню.
</p>
<p>
    В сельской местности всегда требуются: учителя, воспитатели, врачи(это в районных центрах), почтальоны. Колхозы
    страдают от нехватки кадров в животноводстве, механизаторов.
</p>
<p>
    Можно обратится в занятости, они тоже могут организовать переезд в деревню и помочь с трудоустройством. А если у вас
    есть идея, желание открыть свой бизнес на селе, помогут финансово.
</p>
<p>
    Приобретая жилье в сельской местности и земельный участок, получив постоянную регистрацию, вы можете получить статус
    ЛПХ(личного подсобного хозяйства). Если ваш приусадебный участок не более 0,5 га., всю выращенную на нем продукцию,
    вы можете продавать или сдавать без уплаты налогов. Эту тему мы разберем подробнее чуть позже.
</p>
<p>
    Если у вас есть мечта переехать в деревню, пробуйте, быт можно устроить, как в городе, а остальное для удовольствия
    — сад, огород, природа, рыбалка, да просто чистый воздух.
</p>
<h3>Немного суеверия</h3>
<p>
    Старые мудрые люди считали, что, переезжая обязательно необходимо забрать с собой веник, т.к. за ним якобы прячется
    домовой. Это даст возможность домовому переселиться со своими хозяевами и разогнать все неприятности (о которых в не
    догадывались) и привлечь везенье и удачу).
</p>
<p>
    Уезжая, на старом месте жительства необходимо убрать за собой весь мусор. В старину считалось, что на неубранный при
    переезде мусор, новые жильцы могут сделать порчу бывшим хозяевам жилища.
</p>
<p>
    Немаловажное значение имеет погода в день переезда. Если в этот день пойдет дождь, то на новом месте Вас ждет жизнь
    полная приключений и событий, снегопад – обещает богатую жизнь. Солнечная погода в день переезда предсказывает, что
    на новом месте жительства у Вас будет благополучная и спокойная жизнь.
</p>
<p>
    День недели, на который Вы назначите свой переезд, тоже очень важен в этом мероприятии.
</p>
<p>
    Понедельник обещает не дюжее здоровье всем членам семьи на новом месте и поправку от хвори.
</p>
<p>
    Вторник обещает, что после переезда Вы можете заняться своим духовным развитием и встретите много друзей.
</p>
<p>
    Среда – сулит любовь и даже может не одну.
</p>
<p>
    Если переедите на новое место жительства в четверг, то Вам будут сопутствовать богатство и достаток. Главное
    откройте вовремя двери и счет в Сбербанке.
</p>
<p>
    Переезд в пятницу обещает успех в делах. Лучшее время для трудоустройства и начала собственного бизнеса.
</p>
<p>
    Если Вы будете переезжать в выходные дни (субботу и воскресенье), то можете рассчитывать на удачу. Тут главное не
    мешкать.
</p>
<p>
    В любом случае постарайтесь найти кусочек янтаря (с многомиллионной историей и духом древних времен. Он дает силы и
    притягивает только хорошие силы в помощь… Проверьте на практике.
</p>
<p>
    И наконец первой на новое место жительство, нужно пустить кошку. Кошка, являясь посредником между астральным и
    физическими мирами, наладит хорошие отношения с нечистой силой и подружится с домовым. После этого хозяева могут
    смело переступать порог своего жилища. Если кошки нет – то и тут подойдет кусочек янтаря. Из жильцов, в обязательном
    порядке, первым должен переступать порог самый старший член семьи, а за ним уже все остальные члены семьи. Каждый
    гость, прежде чем переступить порог дома, обязательно бросает монетку в новое жилище. Это будет символом денежного
    дождя и богатства для хозяев жилища, который обещает им беспечную жизнь на новом месте.
</p>
<h3>Пенсионное обеспечение при ПМЖ</h3>
<p>
    Если пенсионер переехал жить в другое место, ему необходимо прийти в Управление Пенсионного фонда по новому месту
    жительства и написать заявление на запрос пенсионного дела с прежнего места жительства.
</p>
<p>
    Если гражданин не зарегистрирован по новому месту жительства или месту пребывания на территории Российской
    Федерации, запрос пенсионного дела оформляется на основании письменного заявления с указанием адреса Фактического
    места проживания.
</p>
<p>
    Одновременно с заявлением о запросе пенсионного дела оформляется заявление о доставке пенсии по новому месту
    жительства.
</p>
<p>
    Сотрудники Пенсионного фонда России оформляют запрос пенсионного дела и не позднее одного рабочего дня, следующего
    за днем обращения пенсионера, направляют его в территориальный орган ПФР по прежнему месту жительства гражданина.
</p>
<p>
    Из Пенсионного фонда по прежнему месту жительства пенсионное дело направляется по новому месту жительства не позднее
    трех рабочих дней с момента поступления запроса.
</p>
<p>
    Не позднее двух рабочих дней с момента поступления пенсионного дела оформляется распоряжение о постановке его на
    учет и о продлении выплаты пенсии по новому месту жительства. При этом проверяется правильность установления пенсии
    по прежнему месту жительства на основании документов пенсионного дела.
</p>
<p>
    Такой порядок касается не только переезда, но и случаев, когда пенсионер направляется на постоянное проживание в
    учреждение социальной защиты, дом ветеранов, например.
</p>
<p>
    Запрашивать пенсионное дело в связи с переездом следует также в том случае, если по прежнему месту жительства
    пенсионер получал пенсию на счет банковской карты.
</p>
<p>
    В пенсионном деле содержится вся необходимая информация, которая может понадобиться гражданину и сотруднику ПФР по
    новому месту жительства. К примеру, для перерасчета размера пенсии.
</p>
<p>
    Для граждан, переехавших за пределы Российской Федерации как в дальнее, так и в ближнее зарубежье, установлен другой
    порядок выплаты страховой пенсии.
</p>
<h3>Воинский учет при смене места жительства</h3>
<p>
    Согласно статье 10 ФЗ "О воинской обязанности и военной службе", в целях обеспечения воинского учета граждане
    обязаны: явиться в двухнедельный срок в военный комиссариат для постановки на воинский учет, снятия с воинского
    учета и внесения изменений в документы воинского учета при переезде на новое место жительства, расположенное за
    пределами территории муниципального образования, место пребывания на срок более трех месяцев либо выезде из
    Российской Федерации на срок более шести месяцев или въезде в Российскую Федерацию.
</p>
<p>
    Таким образом, если Вы переехали в другой район, либо в другой город, вам необходимо явится в нынешний военкомат, и
    написать заявление о снятии с учёта, явится в новый военкомат и написать заявление о постановке на учёт.
</p>
<p>
    Неявка гражданина, состоящего или обязанного состоять на воинском учете, для постановки на воинский учет, снятия с
    воинского учета и внесения изменений в документы воинского учета при переезде на новое место жительства, влечет
    предупреждение или наложение административного штрафа в размере от ста до пятисот рублей, согласно статье 21. 5 КоАП
    РФ.
</p>
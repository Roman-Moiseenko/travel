    <br>(часть вторая в ред. Федерального закона от 02.04.2014 N 50-ФЗ)
</p>
<p>
    Оплата стоимости проезда работника и членов его семьи личным транспортом к месту использования отпуска и обратно
    производится по наименьшей стоимости проезда кратчайшим путем.
</p>
<p>

    органа, государственного внебюджетного фонда Российской Федерации или федерального государственного учреждения и
    членов его семьи и обратно производится по заявлению работника не позднее чем за три рабочих дня до отъезда в отпуск
    исходя из примерной стоимости проезда. Окончательный расчет производится по возвращении из отпуска на основании
    предоставленных билетов или других документов.
    <br>(в ред. Федерального закона от 02.04.2014 N 50-ФЗ)
</p>
<p>
    Порядок компенсации расходов на оплату стоимости проезда и провоза багажа к месту использования отпуска и обратно
    для лиц, работающих в федеральных государственных органах, государственных внебюджетных фондах Российской Федерации,

                        Второй природной достопримечательностью является Виштинецкий парк. Он находится в далике от всех
                        туристических маршрутов, но экскурсии к нему все же встречаются у гидов. Парк находится на границе с Литвой,
                        имеет огромное озеро, в котором можно купаться.
                        <br>
                        Какие подарки и сувениры везут гости Калининграда? Разумеется, янтарь. В любом виде, начиная с

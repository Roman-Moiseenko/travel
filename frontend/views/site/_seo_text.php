                        продукцией великое множество как в самом Калининграде, так и в городах-курортах Светлогорск и
                        Зеленоградск. Целые улицы отданы под торговлю янтарем. Помимо сувениров, но меньшим спросом пользуется
                        косметика и оздоровительная продукция из янтаря, которую производят на основе янтарной кислоты.
                        <br>
                        В качестве подарков с Калининграда привозят марципан - это миндальная мука, смешанная с сахарным
